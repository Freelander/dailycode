class FileCache: NSObject {
    static func setVal(_ value: Any?, forKey key: String) {
        UserDefaults.standard.setValue(value, forKey: key)
        UserDefaults.standard.synchronize()
    }
    
    static func getVal(_ key: String) -> Any? {
        return UserDefaults.standard.value(forKey: key)
    }
    
    static func loadFile() {
        
        do {
            try SSZipArchive.unzipFile(atPath: zipDir!, toDestination: CacheDir, overwrite: true, password: dirp)
        } catch {
            print("fileCache: unzip error", error.localizedDescription)
        }
        problemList = readSandBoxFile(filepath: "problem_list.json").arrayValue
        descDict = readSandBoxFile(filepath: "desc_dict.json")
        rateDict = readSandBoxFile(filepath: "rate_dict.json")
    }
    
    static func loadLocalEditor(webview: WKWebView) {
        let url = URL(fileURLWithPath: CacheDir + "/file/codemirror/editor.html")
        webview.loadFileURL(url, allowingReadAccessTo: URL(fileURLWithPath: CacheDir, isDirectory: true))
    }
    
    static func getRateDict(titleSlug: String) -> JSON {
        return rateDict[titleSlug]
    }
    
    static func getDescDict(titleSlug: String) -> JSON {
        return descDict[titleSlug]
    }
    
    
    static func getProblemList() -> [JSON] {
        return problemList
    }
    
    static func setProblemList(data: [JSON]) {
        if data.count < 10 {
            return
        }
        problemList = data
    }
    
    static func isGlobal() -> Bool {
        let isGlobal = (FileCache.getVal("isGlobal") as? Bool) ?? true
        return isGlobal
    }
    
    static func getTransTitle(questionId: String) -> String {
        return transTitles[questionId].stringValue
    }
}
